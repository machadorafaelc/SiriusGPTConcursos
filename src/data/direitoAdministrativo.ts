// Função para buscar conhecimento por tópico
export function buscarConhecimento(topic: string): string {
  const topicLower = topic.toLowerCase();
  
  if (topicLower.includes('poder') || topicLower.includes('hierárquico') || topicLower.includes('disciplinar') || topicLower.includes('regulamentar') || topicLower.includes('polícia')) {
    return conhecimentoDireitoAdministrativo.poderesAdministrativos.conteudo;
  }
  
  if (topicLower.includes('organização') || topicLower.includes('centralização') || topicLower.includes('descentralização') || topicLower.includes('autarquia') || topicLower.includes('empresa pública')) {
    return conhecimentoDireitoAdministrativo.organizacaoAdministrativa.conteudo;
  }
  
  if (topicLower.includes('licitação') || topicLower.includes('contrato') || topicLower.includes('pregão') || topicLower.includes('concorrência')) {
    return conhecimentoDireitoAdministrativo.licitacoesContratos.conteudo;
  }
  
  if (topicLower.includes('ato') || topicLower.includes('atributo') || topicLower.includes('elemento') || topicLower.includes('invalidação')) {
    return conhecimentoDireitoAdministrativo.atosAdministrativos.conteudo;
  }
  
  if (topicLower.includes('processo') || topicLower.includes('recurso') || topicLower.includes('prazo') || topicLower.includes('decisão')) {
    return conhecimentoDireitoAdministrativo.processoAdministrativo.conteudo;
  }
  
  if (topicLower.includes('estatuto') || topicLower.includes('servidor') || topicLower.includes('8.112') || topicLower.includes('concurso') || topicLower.includes('posse') || topicLower.includes('exercício') || topicLower.includes('estágio') || topicLower.includes('estabilidade') || topicLower.includes('provimento') || topicLower.includes('nomeação') || topicLower.includes('promoção')) {
    return conhecimentoDireitoAdministrativo.estatutoServidores.conteudo;
  }
  
  if (topicLower.includes('improbidade') || topicLower.includes('14.230') || topicLower.includes('8.429') || topicLower.includes('enriquecimento') || topicLower.includes('prejuízo') || topicLower.includes('moralidade')) {
    return conhecimentoDireitoAdministrativo.improbidadeAdministrativa.conteudo;
  }
  
  // Retorna conhecimento geral se não encontrar tópico específico
  return `
  Direito Administrativo é o ramo do direito público que regula a atividade administrativa do Estado.
  
  Principais temas:
  - Poderes Administrativos (hierárquico, disciplinar, regulamentar, polícia)
  - Organização Administrativa (centralização, descentralização, autarquias)
  - Licitações e Contratos (Lei 14.133/2021)
  - Atos Administrativos (elementos, atributos, classificação)
  - Processo Administrativo (Lei 9.784/1999)
  - Estatuto dos Servidores (Lei 8.112/90)
  - Improbidade Administrativa (Lei 14.230/21)
  
  Para informações específicas, mencione o tópico desejado.
  `;
}
